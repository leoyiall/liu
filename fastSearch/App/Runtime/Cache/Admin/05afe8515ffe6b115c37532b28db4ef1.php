			<div class="wz_content">
				<img src="images/333.jpg"  class="img-thumbnail" alt="Responsive image">
				<p>我和蜡青在东京的Kyoto-inn等到晚上八点，于婕还是没有回来。之前我们三个分头行动，她说想去东京铁塔登高，晚上和我们一起吃晚饭。</p>

				<p>蜡青很担心：“大家都第一次来东京，她不会迷路了吧，我去找找。”

				<p>我很笃定：“放心，丢不了，我们都丢了她也不会丢，她机灵着呢。八成是邂逅了男人，聊得欢。我们先去吃饭。”

				<p>蜡青瞪大了眼，内心挣扎地跟我去了饭馆。

				<p>吃完饭回酒店的路上，于婕撑着透明的雨伞，倒映着地面湿润如水彩画般的荧光迎面走来。

				<p>“你们知道哇，我刚刚在东京铁塔上遇到一个台湾人，聊了好久。”

				<p>我和蜡青愣了一秒，放声大笑。于婕在我们的笑声中不明所以。

				<p>于婕是我认识的在感情中最勇敢的女孩，我曾经这么以为。

				<p>有一年春节，她爽了我的约，说要去追求幸福了。那个幸福是她朋友的朋友，她没有任何他的联系方式，只知道他会在某一天北京的某一个酒吧唱一首歌，她就爽了我的约坐了五个多小时的高铁去了北京。奇迹当然没有发生，她毫无收获地回来了。

				<p>“我长得不好看，如果再不主动，我连一点机会也没有。不成功就不成功，反正我已经努力过了。”她这么说。

				<p>那天我感到隐约的惧怕，我害怕在我畏缩矜持的情路上，早已浑浑噩噩，不知不觉中失去了天命于我的爱情。

				<p>我曾和朋友说，我喜欢的人一般都会喜欢我。后来想想并不是这样，是我只喜欢先喜欢上我的人。如果爱情是一场冒险，我选择安全地冒险。我从不主动，我不介意有没有机会，不喜欢我的人不在机会这个范畴里。

				<p>我羡慕于婕，因为她有勇气先喜欢一个人。

				<p>后来好几年里，于婕一直单身，事实上从我认识她至今，她一直单着。我原以为这么一个勇于追求幸福的女孩理当比谁都更容易找到真爱。

				<p>于婕和我聊往事，在她的往事里，有两个人，姑且叫A和B吧。A是她的大学同学，她清晰地记得和他的点滴细节，像画面一样叙述给我听。我说你那时候是不是喜欢他？她说是。我说那你没有去告白吗？她说了一堆理由，叭啦叭啦叭啦，总而言之就是告白了也不会有什么结果，所以她选择做朋友。

				<p>另一个B也是她的朋友，认识好多年，直到现在还在她身边，每周都会见面。他们默契，自在，一起旅游，一个电话就能叫出来，见不到会想念。我说你是不是喜欢他？她说她不想失去这份友谊。

				<p>我想我这才真的了解了于婕。

				<p>那些使她勇往直前的并不是爱情，真正的爱情让人退缩。

				<p>她生日之前，B先生说，一起去法国吧，费用我付。

				<p>她跟我说：“可是我工作太忙，只好拒绝了他。”

				<p>我说：“你真的忙吗？”

				<p>她说是真的。

				<p>“你春节还忙吗？”

				<p>“不忙。”

				<p>“那你告诉他，法国你去不了，让他请你春节去北海道吧。”

				<p>她沉默片刻：“要是我会错意怎么办？”

				<p>“会错了意你也只是和你的好朋友一起去北海道玩了一圈。”

				<p>踌躇不前或许很安全，越长大越懂得保护自己，不抱希望就不会失望。但是，爱情就像是信仰，你信了它，它才会向你展示迷人的魔力。

				<p>我们不止一次地在受伤后信誓旦旦地说“我再也不会相信男人”“我再也不会相信女人”“我再也不会相信爱情”，的确，情伤没有伤到身上任何一个器官，却切切实实地让人感到痛，并且留下了恐惧。在西方传说里，恶龙总是与公主、勇士联系在一起。被恶龙囚禁的公主宛如爱情，而恶龙就是心伤与恐惧、想要得到她就必须打倒恶龙，没有勇气的人不配得到她。

				<p>于婕告诉我她和B先生约定了今年春节一起去北海道，去电影《情书》的故乡。

				<p>于婕说：“要是他还是没有行动呢？”

				<p>我说：“如果他一直没有告白，那就在旅程的最后一天拉他的手吧。”

				<p>有一句话，相信奇迹的人才会遇到奇迹。爱情也是。
